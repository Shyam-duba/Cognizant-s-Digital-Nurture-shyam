package org.example;

public class Calculate {
    public int add(int a, int b) {
        int sum = a + b;

        return sum;
    }

    public int subtract(int a, int b) {
        int sub = a - b;

        return sub;
    }

    public int multiply(int a, int b) {
        int mul = a * b;

        return mul;
    }

    public int division(int a, int b){
        int div = a /b;

        return div;
    }
}
